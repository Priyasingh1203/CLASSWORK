package collection;

public class Student1 {
     int id;
     String name;
     String course;
     public Student1(int id, String name, String course)
     {
    	 super();
    	 this.id =id;
    	 this.name=name;
    	 this.course=course;
     }
     public int getId()
     {
    	 return id;
     }
	
     public String getCourse() {
		return course;
	}
	public String getName() {
		return name;
	}
	
	
}
